/**
 * dump 美化输出
 * @param $var
 */
function de_dump($var) {
    echo '<pre>';
    var_dump($var);
    echo '<br>';
    echo '<pre>';
}

/**
 * print_r 美化输出
 * @param $var
 */
function de_print_r($var) {
    echo '<pre>';
    print_r($var);
    echo '<br>';
    echo '<pre>';
}
